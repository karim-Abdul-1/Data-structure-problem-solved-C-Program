// I/O your CGPA. (Take input twice, and give output twice separately)
#include<stdio.h>
int main()
{
    float a,b;
    printf("Enter your Total CGPA  : ");
    scanf("%f",&a);
    printf("Enter your Semester CGPA: ");
    scanf("%f",&b);
    printf("My total CGPA is : %.2f\n",a);
    printf("My semester CGPA is : %.2f\n",b);

    return 0;
}
